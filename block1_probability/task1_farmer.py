"""
Блок 1: Теория вероятности и логика
Задание 1: Фермер

На ферме содержатся шесть разных видов животных, и каждый раз, когда фермер заходит в сарай,
он видит одно случайное животное. За день фермер заходит в сарай 6 раз.

Каково математическое ожидание количества разных видов животных, которые фермер увидит за день?
Ответ округлить до сотых, например: 4,12
"""

import math


def expected_unique_species(n_species=6, n_visits=6):
    """
    Вычисляет математическое ожидание количества разных видов животных,
    которых увидит фермер за n_visits посещений.
    
    Используем линейность математического ожидания:
    E[X] = E[I_1 + I_2 + ... + I_6] = E[I_1] + E[I_2] + ... + E[I_6]
    где I_i - индикатор того, что вид i был увиден хотя бы раз.
    
    P(I_i = 1) = 1 - P(вид i не был увиден ни разу)
    P(вид i не был увиден ни разу) = (5/6)^6
    
    Поэтому E[I_i] = 1 - (5/6)^6
    И E[X] = 6 * (1 - (5/6)^6)
    """
    # Вероятность не увидеть конкретный вид ни разу
    prob_not_seen = ((n_species - 1) / n_species) ** n_visits
    
    # Вероятность увидеть конкретный вид хотя бы раз
    prob_seen_at_least_once = 1 - prob_not_seen
    
    # Математическое ожидание количества разных видов
    expected = n_species * prob_seen_at_least_once
    
    return expected


if __name__ == "__main__":
    result = expected_unique_species(6, 6)
    print(f"Математическое ожидание количества разных видов животных: {result:.2f}")
    print(f"Ответ: {result:.2f}")
