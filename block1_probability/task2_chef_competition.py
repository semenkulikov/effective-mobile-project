"""
Блок 1: Теория вероятности и логика
Задание 2: Кулинарное соревнование

В конкурсе участвуют 80 шеф-поваров с уникальными уровнями мастерства.
В первом этапе судьи случайным образом распределяют их по парам (в любом состязании двух шефов
выигрывает тот, у кого выше уровень мастерства).
На втором этапе шефы снова случайно образуют пары для финального раунда (пары могут повториться).
Победную награду получают те, кто выиграл в обоих этапах.

Каково математическое ожидание числа победителей?
Ответ округлить до десятых, например: 33,5
"""


def expected_winners(n_chefs=80):
    """
    Вычисляет математическое ожидание числа победителей.
    
    Рассуждение:
    - В первом этапе выигрывает ровно половина шефов: 40 человек
    - Во втором этапе из 80 шефов снова случайно образуются пары
    - Вероятность выиграть во втором этапе для каждого шефа = 1/2
    - Вероятность выиграть в обоих этапах = (1/2) * (1/2) = 1/4
    
    Используя линейность математического ожидания:
    E[победителей] = 80 * (1/4) = 20
    
    Однако, нужно учесть, что во втором этапе участвуют все 80 шефов,
    а не только победители первого этапа. Поэтому:
    - Вероятность выиграть в первом этапе = 1/2
    - Вероятность выиграть во втором этапе = 1/2
    - Вероятность выиграть в обоих = (1/2) * (1/2) = 1/4
    
    E[победителей] = 80 * (1/4) = 20
    """
    # Вероятность выиграть в первом этапе
    prob_win_stage1 = 1 / 2
    
    # Вероятность выиграть во втором этапе
    prob_win_stage2 = 1 / 2
    
    # Вероятность выиграть в обоих этапах (события независимы)
    prob_win_both = prob_win_stage1 * prob_win_stage2
    
    # Математическое ожидание числа победителей
    expected = n_chefs * prob_win_both
    
    return expected


if __name__ == "__main__":
    result = expected_winners(80)
    print(f"Математическое ожидание числа победителей: {result:.1f}")
    print(f"Ответ: {result:.1f}")
