"""
Блок 1: Теория вероятности и логика
Задание 3: Одинокая дорога

На пустынном шоссе вероятность появления автомобиля за 30-минутный период составляет 0.95.

Какова вероятность его появления за 10 минут? А за 27 минут?
Ответ дать в процентах, округлив до десятых через точку с запятой, например: 42,7; 95,0
"""

import math


def probability_appearance(time_minutes, prob_30min=0.95):
    """
    Вычисляет вероятность появления автомобиля за заданное время.
    
    Предполагаем, что процесс появления автомобилей подчиняется экспоненциальному распределению
    или модели Пуассона. Если вероятность за 30 минут = 0.95, то:
    
    P(не появился за 30 мин) = 1 - 0.95 = 0.05
    
    Для экспоненциального распределения:
    P(не появился за время t) = e^(-λt)
    
    Для 30 минут: e^(-λ*30) = 0.05
    Отсюда: λ = -ln(0.05) / 30
    
    Для времени t: P(появился за время t) = 1 - e^(-λt)
    """
    # Вероятность не появиться за 30 минут
    prob_not_30min = 1 - prob_30min
    
    # Параметр λ для экспоненциального распределения
    # e^(-λ*30) = prob_not_30min
    # -λ*30 = ln(prob_not_30min)
    # λ = -ln(prob_not_30min) / 30
    lambda_param = -math.log(prob_not_30min) / 30
    
    # Вероятность появления за время time_minutes
    prob_appear = 1 - math.exp(-lambda_param * time_minutes)
    
    return prob_appear


if __name__ == "__main__":
    prob_10min = probability_appearance(10)
    prob_27min = probability_appearance(27)
    
    print(f"Вероятность появления за 10 минут: {prob_10min * 100:.1f}%")
    print(f"Вероятность появления за 27 минут: {prob_27min * 100:.1f}%")
    print(f"Ответ: {prob_10min * 100:.1f}; {prob_27min * 100:.1f}")
